		<p>
			The Customer hereby represents and warrants that:
		</p>
		<p>
			(a) The Customer shall, at all times, ensure compliance with the applicable laws and is not bound by any contract, which may restrict its right or ability to enter into or perform this Agreement, or which would be breached as a result of execution and performance of this Agreement.
		</p>
		<p>
			(b) The Invoice which forms the basis of the creation of rights in favor of Financier under this Agreement is an unpaid, genuine invoice raised by the Seller on the Customer and the Invoice has not been previously listed on the Platform, either by the Customer or the Seller.
		</p>
		<p>
			(c) The Invoice Receivables represent amount due from the Customer to the Seller and the Customer has created absolute, clear, sole, legal and beneficial right, title and interest in the Invoice Receivables in favour of the Financier in accordance with the terms of this Agreement and will not challenge the liability to pay the Invoice Receivables on the Due Date to the Financier.
		</p>
		<p>
			(d) The Invoice Receivables is free and clear of all encumbrances, charges, hypothecations and liens, or any other third-party interest in any manner whatsoever. The Invoice does not permit set-off of any amount due and payable under the Invoice against any other amount payable between the Customer and the Seller. Upon payment of Consideration to the Seller, the Seller shall provide consent to the creation of rights contemplated under this Agreement.
		</p>
		<p>
			(e) The payment obligation of the Customer is not conditional upon performance of any obligations by the Seller and the Invoice Receivables are absolute and unconditional obligations of the Customer. On the Effective Date, the Invoice Receivables have not already been paid by the Customer to the Seller.
		</p>
