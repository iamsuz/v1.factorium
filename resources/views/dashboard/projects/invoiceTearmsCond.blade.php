		<h3>INVOICE FACTORING TERMS AND CONDITIONS</h3>

		<p>This agreement for a Factoring arrangement (“<b>Agreement</b>”) is entered into on this <b>{{$investment->created_at}}</b> (“<b>Effective Date</b>”);</p>
		<p>
			Factorium operates an online technology platform, <a href="https://factorium.co">www.factorium.co</a> (the “Platform”), which facilitates invoice discounting and reverse invoice discounting transactions between sellers, customers and Financiers registered on the Platform. The Seller, the Customer and the Financier are registered on the Platform.
		</p>
		<p>
			The Seller raises invoices on the Customer typically on a regular basis with respect to payments to be made by the Customer to the Seller for such sales and availing services.
		</p>
		<p>
			Pursuant to the Customer’s registration on the Platform, the Parties have agreed that the Customer shall list the Invoice (as defined below) on the Platform. Pursuant to the mutual agreement between the Parties, the Seller has agreed for discounting of Invoices of the Customer for the creation of the right and interest in the Invoice Receivables in favour of the Financier.
		</p>
		<p>
			In furtherance of the same, the Parties are desirous of entering into this Agreement for the purpose of recording the terms and conditions with respect to the creation of rights in favour of the Financier. Financier and Customer have authorized Factorium to act as the administrator for the purpose of settling the amounts between the Parties, pursuant to this Agreement. Thereby, Factorium is desirous of executing this Agreement in the capacity of an administrator.
		</p>
		<h4>
			NOW THEREFORE, THE PARTIES AGREE AS FOLLOWS:
		</h4>
		<h5>
			DEFINITIONS AND INTERPRETATION:
		</h5>
		<p>
			Definitions and Interpretation not mentioned hereunder shall have the same meaning as Terms and Conditions on the Factorium platform.
		</p>
		<p>
			(a) “Invoice Receivables” shall mean the receivables under an Invoice, as may be agreed to be acquired by the Financier, for the Consideration listed on the Platform, herein mentioned in Annexure A.
		</p>
		<p>
			(b) “Financier Account” means the bank account of the Financier as registered in the records of Factorium.
		</p>
		<p>
			(c) “Financier Services Agreement” means the Financier services agreement entered into between the Financier and Factorium.
		</p>
		<p>
			(d) “Financier Service Fee” shall mean the fee payable to Factorium by the Financier for the provision of services, as provided in the Financier Services Agreement.
		</p>
		<p>
			(e) “Seller Account” means the bank account of the Seller as registered in the records of Factorium.
		</p>
		<p>
			(f) “Settlement account” means the virtual bank account of the Seller maintained with and managed by Factorium.
		</p>
		<p>
			(g) “Service Fee” means the amount to be paid by the Customer to Factorium for the provision of services by Factorium to the Customer.
		</p>
		<p>
			(h) “Due Date” shall mean the date on which the Invoices become due and payable by the Customer.
		</p>
		<p>
			(i) Transaction Documents” shall mean this Agreement, Creation of Rights Agreement, and such other agreement as may be entered into by the Customer, a Seller and a Financier in connection with the transactions contemplated to be undertaken on the Platform.
		</p>
		<p>
			(j) “Consideration” shall mean as specified in Annexure A.
		</p>

		<h5>1. REGISTRATION ON PLATFORM</h5>
		<p>
			1.1 The Customer shall, in consideration of the provision of services, i.e. invoice discounting by Factorium, pay to Factorium, the Service Fee, for the creation of rights in favor of the Financier under the Invoice as consented by Seller, in terms of this Agreement.
		</p>
		<p>
			1.2 It is hereby expressly clarified that Factorium merely provides a Platform for facilitating the creation of rights in respect of the Financier, the Customer and the Seller and that it is not a borrowing arrangement.
		</p>
		<p>
			1.3 Financier shall have a recourse on Customer for any delay and/or default. Factorium may a beneficial interest in the transaction, in addition to the extent of the Service Fee receivable from the Customer and the Financier. Any interests will be disclosed to the Financier and operations in the event of related parties will be on a commercial arm's length basis. The final decision to create rights in respect of an Invoice shall in all events, be with the Customer, subject to the terms of this Agreement.
		</p>
		<p>
			1.4 Factorium operate under ASIC Corporations (Factoring Arrangements) Instrument 2017/794 as a provider of services under the terms of a factoring arrangement.
		</p>
		<h5>2. AGREEMENT BETWEEN THE PARTIES</h5>
		<p>
			2.1 The Seller has consented to the creation of the right and interest in the Invoice Receivables in favor of the Financier for the Consideration.
		</p>
		<p>
			2.2 Ownership of Invoice Receivables:
		</p>
		<p>
			(a) The Financier acknowledges that the right and interest in the receivables under the Invoice will be owned fully in an indivisible manner. These rights may not be sold in a piecemeal manner or fractionalized further.
		</p>
		<p>
			(b) Upon execution of this Agreement, the Financier shall be the full owner of the Invoice Receivables.
		</p>
		<p>
			(c) The sale made by the Seller is on recourse basis, that is to say, where the Customer does not pay the Invoice Receivables by the Due Date, the Sale shall stand terminated and the Financier shall be entitled to receive the amounts as set out in Clause 5 of this Agreement
		</p>
		<p>
			2.3 The Parties agree that the right and interest in the Invoice Receivables under the Invoice stands created in favor of the Financier to such extent and on the terms and conditions as contained the Transaction Documents. On and from the Effective Date, the terms and conditions of the agreement for sale of products or provision of services between the Customer and Seller shall be deemed incorporated by reference into this Agreement. Simultaneously, the terms and conditions of the Invoice pursuant to which the Invoice was raised, shall stand extinguished and Seller and Customer are discharged from their obligations thereunder.
		</p>
		<p>
			2.4 Factorium shall not appropriate the Consideration and the Invoice Receivables, in any other manner, other than the manner specified herein and as provided under the Transaction Documents. With the exception of any Service Fee due to Factorium under the Transaction Documents, Factorium is merely acting in the capacity of an administrator for the sums involved under the Transaction Documents.
		</p>
