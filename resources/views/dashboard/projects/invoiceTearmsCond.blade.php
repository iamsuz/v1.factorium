		</p>
		<p>
			(h) The Seller and Customer hereby represents and warrants confirming to the Financier that during the validity of this Agreement if it comes to the notice of the Seller that there exists related party transaction, the Seller and the Customer undertakes to inform the Financier and Factorium accordingly on immediate basis.
		</p>

		<h4>3.2 Covenants of Factorium.</h4>
		<p>
			(a) Factorium merely provides a platform for the Seller, the Customer and the Financier to enter into transactions with respect to creation of rights in respect of an Invoice. No act of Factorium as a facilitator shall be construed as legal advice or recommendation with respect to the Invoices or the creation of rights in respect of the Invoices. Where related party transactions might emerge they will be disclosed to the Financier and will be handled on commercial arm's length basis terms.
		</p>
		<p>
			(b) Factorium is only bound by and to the extent of its obligations expressly set out in this Agreement and the other Transaction Documents to which Factorium is a party. Factorium shall not be bound by the provisions of any other agreement or understanding between the Seller, the Customer and/or the Financier. No implied or fiduciary duties or obligations of Factorium shall be read into the terms of this Agreement or any of the Transaction Documents.
		</p>
		<p>
			(c) Factorium is not underwriting / guaranteeing the performance of the obligations by the Customer, the Seller and the Financier under the Transaction Documents. The Parties shall have no recourse whatsoever against Factorium for any default / non-compliance by the other Parties.
		</p>

		<h4>4. SETTLEMENT OF AMOUNTS</h4>
		<p>
			4.1 Factorium consents to act as the administrator for the settlement of amounts between the Financier and Customer pursuant to the creation of rights in favor of the Financier under this Agreement. To this extent, Factorium agrees to be bound by the terms of the Financier Services Agreement and the Customer Services Agreement respectively with respect to the remittance and settlement of money between the Parties.
		</p>
		<p>
			4.2 <b>Settlement of Invoice Receivables.</b> When the Customer remits the amount due under the Invoice into the Settlement Account, and such amounts are transferred into the Settlement account, Factorium shall issue instructions to transfer the Invoice Receivables to the Financier in the manner provided under the Transaction Documents.
		</p>
		<p>
			4.3 Settlement in case of part payment by the Customer. In the event the Customer pays into the Settlement Account, an amount which is less than the amount payable by the Customer under the Invoice:
		</p>
		<p>
			(a) Where no other Financier (s) registered on the Platform has a right and interest in the receivables under the Invoice, Factorium shall forthwith issue instructions to remit such amount as is payable to the Financier under this Agreement in the manner provided under the Transaction Documents, and Factorium and the Financier exercise the remedy available under Clause 5 for recovery of the balance Invoice Receivables payable to the Financier ; and
		</p>
		<p>
			(b) Where the right and interest in the receivables under the Invoice, other than the Invoice Receivables, is created in favor of any other Financier (s) registered on the Platform, Factorium shall issue instructions to disburse to the Financier in the manner provided under the Transaction Documents, such amount from the amount transferred by the Customer into the Settlement Account in the same proportion as against the Invoice Receivables value, procured by the Financier(s) including the Financier, under the Invoice. Factorium and the Financier shall exercise the remedy available under Clause 5 for recovery of the balance Invoice Receivables payable to the Financier.
		</p>
		<p>
			(c) In either case, the Financier Service Fee shall be deducted by Factorium only when the Financier receives the entire Invoice Receivables payable under the Invoice.
		</p>
		<p>
			4.4 It is hereby expressly clarified that Factorium is merely an administrator and Factorium shall not be liable to pay
		</p>
		<p>
			(i) the Invoice Receivables, subject to the deduction of the FinancierService Fee, to the Financier unless the same is remitted into the Settlement Account by the Customer; or
		</p>
		<p>
			(ii) the Consideration, subject to the deduction of the Seller Service Fee, to the Seller, unless the Consideration is remitted into the Collection Account by the Financier.
		</p>
		<h4>5. DEFAULT AND RESTITUTION</h4>

		<h4>5.1 Material Breach by the Customer and Seller:</h4>
		<p>
			(a) In the event the Invoice Receivables is not remitted into the Settlement Account by the Customer by the Due Date for any reason other than breach of Seller representations and warranties under this Agreement, such default shall be deemed to be a material breach of this Agreement by the Customer and should not relieve Customer obligations mentioned in Agreement.
		</p>
		<p>
			(b) In the event, Factorium determines any material breach not limited to any illegal activity committed by Customer before due date, Factorium can encash security cheque for restitution with prior intimation.
		</p>
		<p>
			(c) Pursuant to the termination, the Customer shall be liable to refund the Consideration to the Financier. The Parties agree that such payment is, fair compensation to the Financier for the benefit derived by the Seller from concluding the Sale of Invoice Receivables at a discount and raising working capital through supplier financing prior to the due date under the Invoice. The Customer shall also be liable to pay an interest at the rate of 1% per month (one percent) on the outstanding Consideration Amount which will be charged over and above the discount rate agreed with customer (“Late Payment Fee”). The refund of the Consideration, compensation for the benefit and payment of Late Payment Fee shall together conclude the restitution of the Financier (“Restitution”), and the Customer shall pay such amounts into the Settlement Account.
		</p>
		<p>
			(d) In order to effect restitution, the Customer shall, on the Effective Date, deposit with Factorium, Security cheques drawable on the Customer’s bank account in favor of the Settlement account (“Security Cheque”) together with the Late Payment Fee (“Restitution Amount”). It is hereby expressly clarified that the Security Cheque shall be provided by the Customer to Factorium, for the benefit of the Financier. The obligation of the Customer under the Security Cheque shall be independent and exclusive of the obligation of the Customer to ensure Restitution of the Financier. In the event the Security Cheque is unable to be encashed by Factorium, the obligation of the Customer to undertake the Restitution shall survive.
		</p>
		<p>
			(e) If the Customer fails to remit the Invoice Receivables and Late Payment Fee mentioned within a period of 15 (Fifteen) Business Days from the Due Date into the Settlement Account, Factorium shall, for the benefit of the Financier,<br>
			&nbsp;&nbsp;&nbsp;(i) encash the Security Cheque to the extent of the Restitution Amount;<br>
			&nbsp;&nbsp;&nbsp;(ii) notify the Financier of the encashment; and<br>
			&nbsp;&nbsp;&nbsp;(iii) issue instructions to transfer the Restitution Amount to the Financier Account, after deduction of the Financier Service Fee.
		</p>
		<p>
			(f) In the event Factorium is unable to encash the Security cheque due to lack of sufficient funds in the Customer’s account or for any other reason, the Parties agree that Factorium shall have the right to initiate proceedings against the Customer for the benefit of the Financier. This right shall be without prejudice to any other right that the Financier may have against the Customer in law or equity.
		</p>
		<p>
			(g) In the event the Invoice Receivables is not remitted into the Settlement Account by the Customer by the Due Date for breach of Seller representations and warranties under this Agreement, such default shall be deemed to be a material breach of this Agreement by the Seller.
		</p>
		<p>
			(h) Factorium shall initiate proceedings if found guilty under any provision of Law, as may be appropriate and essential, at the sole responsibility, risk, cost and consequences of the Customer and Seller including with respect to all legal costs and fees borne by the Party that will adjudged to be in default.
		</p>
		<p>
			(i) This Clause outlines all the Financier’s rights and remedies in connection with the recovery of the Invoice Receivables.
		</p>
		<p>
			(j) In case of failure of Customer to fulfill its payment obligations under this Agreement, there shall be no recourse against Seller for whatsoever reason except material breach by Seller.

		</p>

